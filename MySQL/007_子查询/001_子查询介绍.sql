-- 进阶7.子查询介绍
/*
 含义:
出现在其他语句中的select语句,称为子查询或内查询
外部的查询语句,称为主查询或外查询

 分类:
   按子查询出现的位置:
       SELECT后面:
            仅仅支持标量子查询
       FROM后面
            支持表子查询
       WHERE或having后面★
            标量子查询(单行)√
            列子查询 (多行)√
            行子查询(少)
       EXISTS后面(相关子查询)
   按结果集的行列数不同:
       标量子查询(结果集只有一行一列)
       列子查询(结果集只有一列多行)
       行子查询(结果集有一行多列)
       表子查询(结果集一般为多行多列)

*/
-- 一、where或having后面
/*
1、标量子查询（单行子查询）
2、列子查询  （多行子查询）
3、行子查询  （多行多列）
特点：
a.子查询放在小括号内
b.子查询一般放在条件的右侧
c.标量子查询,一般搭配着多行操作符使用
> < >= <= = <>
列子查询,一般搭配着多行操作符使用
IN,ANY/SOME,ALL
*/
